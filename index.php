               <p>Blood sustains precious human life. Its presence and flow allow us to seek the purpose of our existence and attain life’s milestones. Our mission is to support our healthcare partners with adequate, safe, cost-effective blood supplies and services. When we share blood, healing happens; individuals thrive; and families remain whole. Giving blood enriches our life experience, casting aside our differences to link the human race through a common, natural bond.</p>
           </div>
            <div class="row">
                <div class="col-md-6 text">
                    <h2>About Blood Doners</h2>
            <p>Your blood saves lives. Your blood donation is an amazing gift to people who need it in an emergency or for on-going medical treatment. 

           <p> Sometimes it is not possible to give blood, or we may ask you to wait before donating again. To save you a wasted journey, please read this list of the most common reasons people cannot give blood.
           If you have a medical condition, or a question about whether you can give blood, you should check the health & eligibility and travel section before you book an appointment. And if you need to cancel your appointment please give us 3 days' notice so that we can offer the space to another donor. You can easily cancel or reschedule by signing in to your online account or using the NHSGiveBlood app..</p>
           <p>Eating regularly before donating will help to keep your blood sugar levels stable. This is important so that you don’t feel lightheaded or dizzy after your donation. Having a snack before you donate can help maintain these blood sugar levels. Ensuring that your diet contains foods rich in iron - such as meats and green leafy vegetables - will help to keep you feeling well during and after donation..</p>
            <p>Almost half of the blood that you donate is made up of water. The fluids that you lose during donation can cause a drop in blood pressure – causing you to feel faint and dizzy.
                    To help prevent this from happening we ask you to drink 500ml of water immediately before you donate - we’ll give this to you before you donate. Please help us reduce our plastic usage by bringing your own refillable bottle.It’s also important to ensure that you are well hydrated in the days leading up to your donation. This will help to compensate for the fluids lost during donation, and will help to bring your blood volume levels back to normal.It is essential to avoid alcohol before and after donating as this may affect hydration levels and delay recovery..</p>
